-- Crear base de datos
CREATE DATABASE HospitalLuzDeVida;
USE HospitalLuzDeVida;

-- Tabla Pacientes
CREATE TABLE Pacientes (
    Id_Pacientes INT PRIMARY KEY,
    Nombre_Pacientes VARCHAR(100),
    Apellido_Pacientes VARCHAR(100),
    FechaNac_Pacientes DATE,
    Genero_Pacientes VARCHAR(15), 
    Direccion_Pacientes VARCHAR(30),
    Telefono_Pacientes VARCHAR(10),
    Email_Pacientes VARCHAR(40)
);

-- Tabla Medicos
CREATE TABLE Medicos (
   Id_Medicos INT  PRIMARY KEY,
   Nombre_Medicos VARCHAR(20),
   Apellido_Medicos VARCHAR(20),
   Especialidad_Medicos VARCHAR(30),
   HoraAtencion_Medicos VARCHAR(20)
);

-- Tabla Consultorios
CREATE TABLE Consultorios (
  Id_Consultorios BIGINT PRIMARY KEY,
  Nombre_Consultorios VARCHAR(40),
  Ubicacion_Consultorios VARCHAR(25),
  HoraAtencion_Consultorios VARCHAR(20)
);

-- Tabla MedicoConsultorio (Relación muchos a muchos entre Medicos y Consultorios)
CREATE TABLE MedicoConsultorio (
    Id_MedicoConsultorio INT  PRIMARY KEY,
    Id_Medicos INT,
    Id_Consultorios BIGINT,
    FOREIGN KEY (Id_Medicos) REFERENCES Medicos(Id_Medicos),
    FOREIGN KEY (Id_Consultorios) REFERENCES Consultorios(Id_Consultorios)
);

-- Tabla Agenda (contiene horarios de los médicos)
CREATE TABLE Agenda (
    Id_Agenda INT  PRIMARY KEY,
    Id_Medicos INT,
    Fecha_Agenda DATE,
    HoraInicio_Agenda  VARCHAR(15),
    HoraFin_Agenda  VARCHAR(15),
    FOREIGN KEY (Id_Medicos) REFERENCES Medicos(Id_Medicos)
);

-- Tabla Citas (relación entre Pacientes y Medicos a través de Agenda)
CREATE TABLE Citas (
    Id_Citas INT  PRIMARY KEY,
    Id_Pacientes INT,
    Id_Agenda INT,
    FechaHora_Citas VARCHAR(45),
    Motivo_Citas VARCHAR(200),
    Estado_Citas VARCHAR(20),
    FOREIGN KEY (Id_Pacientes) REFERENCES Pacientes(Id_Pacientes),
    FOREIGN KEY (Id_Agenda) REFERENCES Agenda(Id_Agenda)
);

-- Tabla HorarioCitas (para agendar intervalos específicos dentro de las horas de la Agenda)
CREATE TABLE HorarioCitas (
    Id_HorarioCitas INT  PRIMARY KEY,
    Id_Pacientes INT,
    Id_Citas INT,
    Id_Agenda INT,
    FechaHora_Citas VARCHAR(45),
    Motivo_Citas VARCHAR(200),
    Resultado_HorarioCitas VARCHAR(300),
    FOREIGN KEY (Id_Pacientes) REFERENCES Pacientes(Id_Pacientes),
    FOREIGN KEY (Id_Citas) REFERENCES Citas(Id_Citas),
    FOREIGN KEY (Id_Agenda) REFERENCES Agenda(Id_Agenda)
);
